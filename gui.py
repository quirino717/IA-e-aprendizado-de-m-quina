# Constants for the screen size and cell size
SCREEN_WIDTH = 660
SCREEN_HEIGHT = 660
CELL_SIZE = 33

# Initialize Pygame
pygame.init()

# Set up the screen
screen = pygame.display.set_mode((SCREEN_WIDTH, SCREEN_HEIGHT))
pygame.display.set_caption("Wavefront Demo")

# Set up the colors
BLACK = (0, 0, 0)
WHITE = (255, 255, 255)
GREEN = (0, 255, 0)
RED = (255, 0, 0)


# Function to draw a cell at a given position
def draw_cell(x, y, color):
  pygame.draw.rect(screen, color, pygame.Rect(x * CELL_SIZE, y * CELL_SIZE, CELL_SIZE, CELL_SIZE))


# Function to draw the map and the path
def draw_map(map):
  # Draw the map
  for i in range(len(map)):
    for j in range(len(map[0])):
      if map[i][j] == 0:
        color = WHITE
      elif map[i][j] == 2:
        color = GREEN
      elif map[i][j] == 3:
        color = RED
      else:
        color = BLACK
      draw_cell(j, i, color)


# Main game loop
running = True
while running:
  for event in pygame.event.get():
    if event.type == pygame.QUIT:
      running = False

  # Draw the map
#   draw_map(map, caminho)
    draw_map(map)

  # Update the display
  pygame.display.flip()

# Quit Pygame
pygame.quit()
